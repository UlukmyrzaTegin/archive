package rts.forms;

import rts.tiles.MainTiles;

import com.vaadin.annotations.AutoGenerated;
import com.vaadin.ui.AbsoluteLayout;
import com.vaadin.ui.CustomComponent;
import com.vaadin.ui.MenuBar;
import com.vaadin.ui.MenuBar.MenuItem;

import rts.appUI.ArchiveUI;

@SuppressWarnings("serial")
public class OperMenu extends CustomComponent {

	/*- VaadinEditorProperties={"grid":"RegularGrid,20","showGrid":true,"snapToGrid":true,"snapToObject":true,"movingGuides":false,"snappingDistance":10} */

	private AbsoluteLayout mainLayout;
	private MenuBar menuBar;
	private MainTiles mainUI;
	private ArchiveUI app;
	
	public OperMenu(MainTiles mainUI, ArchiveUI app) {
		this.app = app;
		this.mainUI = mainUI;
		buildMainLayout();
		setCompositionRoot(mainLayout);
	}

	@AutoGenerated
	private AbsoluteLayout buildMainLayout() {
		// common part: create layout
		mainLayout = new AbsoluteLayout();
		mainLayout.setImmediate(false);
		mainLayout.setWidth("100%");
		mainLayout.setHeight("100%");
		
		// top-level component properties
		setWidth("100.0%");
		setHeight("100.0%");
		
		// menuBar
		menuBar = new MenuBar();
		menuBar.setImmediate(false);
		menuBar.setWidth("100.0%");
		menuBar.setHeight("-1px");
		MenuBar.MenuItem docsMenu = menuBar.addItem("Журнал", null, null);
		MenuBar.Command mycommand = new MenuBar.Command() {
			@Override
			public void menuSelected(MenuItem selectedItem) {
		        mainUI.getBodyLayout().removeAllComponents();
		        if (selectedItem.getText() == "Журнал") {
		        	mainUI.getBodyLayout().addComponent(new OperView(app));
		        	
				}
		        
			}
		};

		docsMenu.addItem("-", null, null);
		docsMenu.addItem("Журнал", null, mycommand);
		docsMenu.addItem("-", null, null);
		mainLayout.addComponent(menuBar, "top:0.0px;left:0.0px;");
		menuBar.setImmediate(true);
		return mainLayout;
	}

}
